					<span>
						<b class="title"><span class="redTed">TEDx</span>BITSGoa </b>
						</span></br></br><span><span class="redTed">TEDx</span>BITSGoa aims at bringing forth the most basic form of <i>effective</i> communication. It often takes an idea lot of time to seep into our mind. The more it is related to the day to day life of a person, the easier it is for the latter to grasp the essence of the idea.
						</span>
						</br>
						<span>Core to our goal is a belief that there is no greater force for changing the world than a powerful idea.
						</br>
						Consider : 
						<ul>
							<li>An idea can be created out of nothing except an inspired imagination.</li>
							<li>An idea weighs nothing.</li>
							<li>It can be transferred across the world at the speed of light for virtually zero cost</li>
							<li>And yet an idea, when received by a prepared mind, can have extraordinary impact.</li>
							<li>It can reshape that mind's view of the world.</li>
							<li>It can dramatically alter the behaviour of the mind's owner.</li>
							<li>It can cause the mind to pass on the idea to others.</li>
						</ul>
					</span>
					</br>
					<span><b  class="title">Mission</b></span>
					<span>
						</br>
						</br>
						<span class="redTed">TEDxBITSGoa</span>'s passion and mission is to reach out to people to learn, share and discuss ideas that have the power and potential to Make A Difference in our lives and in our world.
					</span>
					</br>
					</br>
					<span><b class="title">What is <span class="redTed">TEDx</span>?</b></span>
					</br>
					</br>
					<span>
						In the spirit of <i>Ideas Worth Spreading</i>, <span class="redTed">TED</span> has created a program called <span class="redTed">TEDx</span> that enables schools, businesses, libraries or just groups of friends to enjoy a <span class="redTed">TED</span> like experience through events they themselves organise, design and host.
					</span>
					</br>
					<span>
						At a <span class="redTed">TEDx</span> event, live presenters and <span class="redTed">TED</span> Talks combine to spark deep discussions and connections in a small group. Although, <span class="redTed">TED</span> is supporting approved organisers by offering a free toolset that includes detailed guidelines, individual <span class="redTed">TEDx</span> events are self-organised.
					</span>
					</br>
					<span>
						In the very first year(2009-2010) more than 800 <span class="redTed">TEDx</span> events were organised in over 25 languages and 80 countries around the world.
						<span class="redTed">TEDx</span> is part of a revolution in global education made possible by local organisers wanting to engage their communities, inspire innovation thought leadership and visionary insight.
					</span>
					</br>
					</br>
					<span>
						<b class="title">WHAT IS <span class="redTed">TED </span>?</b></span>
					</br>
					</br>
					<span>
						<span class="redTed">TED</span> is a global non-profit organisation focussing on <i>Ideas Worth Spreading</i>. <span class="redTed">TED</span> was started in 1984 as a Small conference bringing together people from three worlds: Technology, Entertainment, and Design, to talk about how their ideas were converging. Since then, <span class="redTed">TED</span>'s scope has become broader, encompassing today the whole spectrum of human ingenuity, creativity, and society.
					</span>
					</br>
					</br>
					<span>Today, <span class="redTed">TED</span> is a global platform for disseminating great ideas and good thinking, using conferences (the annual <span class="redTed">TED</span> and <span class="redTed">TEDGlobal</span> are considered among the highest level gatherings of doers and thinkers in the world), a very popular website (<a href="http://www.ted.com"class="redTed">TED.com</a>) attracting audiences of millions watching <span class="redTed">TED</span> Talks subtitled in 70+ languages, a fellowship, a prize, extensive activities on social networks, and a series of hundreds of independently organised events taking place all over the world under the name <span class="redTed">TEDx</span>, including  <span class="redTed">TEDxBITSGoa</span>.
						</span>
					</br>
					</br>
					<span>
						The <span class="redTed">TED</span> Conference,  held annually in Long Beach, is still the heart of <span class="redTed">TED</span>. Over four days, 50 speakers each take an 18-minute slot to share an idea to change the world. More than a thousand people now attend -- indeed, the event sells out a year in advance -- and the content has expanded to include science, politics, business, the arts and the global issues facing our world. There are no breakout groups. Everyone shares the same experience. It shouldn't work, but it does. It works because all knowledge is connected.  At <span class="redTed">TED</span>, Bill Gates unleashed a crowd of mosquitoes into the audience, yet it was also the venue for Al Gore to launch his climate change campaign whilst Bono and Richard Branson, as well as Bill Clinton have graced its stage.
						</span>
						</br>
					</br>
					<span>
						In 2006 <span class="redTed">TED</span> took the extraordinary step of making its talks available to the world, at no charge, over the internet. <span class="redTed">TEDTalks</span> began as a simple attempt to share what happens at <span class="redTed">TED</span> with the world. Under the moniker <i>Ideas worth spreading</i>, talks were released online. They rapidly attracted a global audience in the millions.  The reaction was so enthusiastic that the entire <span class="redTed">TED</span> website has been reengineered around <span class="redTed">TED</span> Talks, with the goal of giving everyone on demand access to the world's most inspiring voices. More than 150 million video downloads by more than 15 million people worldwide is the clear sign of the success of the Ideas worth Spreading program. 
						</br>
